#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { spawnSync } from 'node:child_process';

import { skillDefinitions } from '../src/scripts/workflows/skill-manifest.mjs';
import { VERSION } from '../src/scripts/lib/version.mjs';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');

function parseArgs(argv) {
  const args = { _: [] };

  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];

    if (arg.startsWith('--')) {
      if (arg.includes('=')) {
        const idx = arg.indexOf('=');
        const key = arg.slice(2, idx);
        const value = arg.slice(idx + 1);
        args[key] = value;
        continue;
      }

      const key = arg.slice(2);
      const next = argv[i + 1];

      if (next === undefined || next.startsWith('--')) {
        args[key] = true;
      } else {
        args[key] = next;
        i += 1;
      }
    } else {
      args._.push(arg);
    }
  }

  return args;
}

function fail(message) {
  console.error(`[deploy-to-agent] ${message}`);
  process.exit(1);
}

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function rmrf(target) {
  fs.rmSync(target, { recursive: true, force: true });
}

function copyDir(src, dest) {
  if (!fs.existsSync(src)) {
    fail(`Missing source directory: ${src}`);
  }

  ensureDir(path.dirname(dest));
  fs.cpSync(src, dest, { recursive: true });
}

function indent(text, spaces) {
  const pad = ' '.repeat(spaces);

  return String(text || '')
    .split('\n')
    .map((line) => (line.trim() === '' ? '' : `${pad}${line}`))
    .join('\n');
}

function renderCommands(commands) {
  return commands
    .map((command) => ['```bash', command, '```'].join('\n'))
    .join('\n\n');
}

function renderSteps(steps) {
  return steps
    .map((step, idx) => `${idx + 1}. **${step.name}** — ${step.text}`)
    .join('\n');
}

const SKILL_TEMPLATE = [
  '---',
  'name: {{NAME}}',
  'description: |',
  '{{DESCRIPTION_INDENTED}}',
  '---',
  '',
  '# {{TITLE}}',
  '',
  'Generated by agentic-sdlc. Do not edit step details manually.',
  '',
  '## CLI',
  '',
  '```bash',
  '{{CLI_VAR}}',
  '```',
  '',
  'If SDLC_CLI is not set, use:',
  '',
  '```bash',
  'node {{CLI_PATH}}',
  '```',
  '',
  '## Overview',
  '',
  '{{OVERVIEW}}',
  '',
  '## Workflow',
  '',
  'Run:',
  '',
  '```bash',
  'node "$SDLC_CLI" {{WORKFLOW}}',
  '```',
  '',
  'Follow the `instructions` field returned by the CLI.',
  '',
  '## Steps',
  '',
  '{{STEPS}}',
  '',
  '## Commands',
  '',
  '{{COMMANDS}}',
  '',
  '## Templates',
  '',
  'Templates are installed at:',
  '',
  '```text',
  '{{TEMPLATES_ROOT}}',
  '```',
  '',
].join('\n');

function renderSkill(skill, vars) {
  return SKILL_TEMPLATE.replaceAll('{{NAME}}', skill.id)
    .replaceAll('{{DESCRIPTION_INDENTED}}', indent(skill.description, 2))
    .replaceAll('{{TITLE}}', skill.title)
    .replaceAll('{{CLI_VAR}}', `SDLC_CLI="\${SDLC_CLI:-${vars.cliPath}}"`)
    .replaceAll('{{CLI_PATH}}', vars.cliPath)
    .replaceAll('{{OVERVIEW}}', skill.overview)
    .replaceAll('{{WORKFLOW}}', skill.workflow)
    .replaceAll('{{STEPS}}', renderSteps(skill.steps))
    .replaceAll('{{COMMANDS}}', renderCommands(skill.commands))
    .replaceAll('{{TEMPLATES_ROOT}}', vars.templatesRoot);
}

function main() {
  const args = parseArgs(process.argv.slice(2));

  const destArg = args.dest || args._[0];

  if (!destArg) {
    fail(
      'Usage: deploy-to-agent --dest <path-to-agent-root> ' +
        '[--project-root <path>] [--runtime-dir sdlc] [--skills-dir skills] ' +
        '[--clean] [--bundle] [--install] [--skip-smoke]'
    );
  }

  const destAbs = path.resolve(String(destArg));

  const projectRootAbs = args['project-root']
    ? path.resolve(String(args['project-root']))
    : path.basename(destAbs).startsWith('.')
      ? path.dirname(destAbs)
      : process.cwd();

  const runtimeDir = String(args['runtime-dir'] || 'sdlc');
  const skillsDir = String(args['skills-dir'] || 'skills');

  const runtimeAbs = path.join(destAbs, runtimeDir);
  const skillsAbs = path.join(destAbs, skillsDir);

  const agentRootRelative = path.relative(projectRootAbs, destAbs);

  const agentPrefix = agentRootRelative
    ? `${agentRootRelative.split(path.sep).join(path.posix.sep)}/`
    : '';

  const cliPath = `${agentPrefix}${runtimeDir}/scripts/sdlc.mjs`;
  const runtimeRoot = `${agentPrefix}${runtimeDir}`;
  const templatesRoot = `${agentPrefix}${runtimeDir}/templates`;

  const manifestPath = path.join(runtimeAbs, 'manifest.json');

  if (args.clean) {
    let oldManifest = null;

    if (fs.existsSync(manifestPath)) {
      try {
        oldManifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
      } catch {
        oldManifest = null;
      }
    }

    const oldSkills = Array.isArray(oldManifest?.skills)
      ? oldManifest.skills
      : [];

    for (const skillId of oldSkills) {
      rmrf(path.join(skillsAbs, skillId));
    }

    rmrf(runtimeAbs);
  }

  ensureDir(destAbs);
ensureDir(projectRootAbs);

  let bundled = false;

  if (args.bundle) {
    const buildResult = spawnSync(
      process.execPath,
      [path.join(root, 'bin', 'build-runtime.mjs')],
      {
        stdio: 'inherit',
      }
    );

    if (buildResult.status !== 0) {
      fail('Runtime build failed.');
    }

    const bundleFile = path.join(root, 'dist', 'sdlc.mjs');

    if (!fs.existsSync(bundleFile)) {
      fail(`Missing bundle file: ${bundleFile}`);
    }

    ensureDir(path.join(runtimeAbs, 'scripts'));

    fs.copyFileSync(
      bundleFile,
      path.join(runtimeAbs, 'scripts', 'sdlc.mjs')
    );

    bundled = true;
  } else {
    copyDir(
      path.join(root, 'src', 'scripts'),
      path.join(runtimeAbs, 'scripts')
    );

    const rootPackagePath = path.join(root, 'package.json');
    const rootPackage = fs.existsSync(rootPackagePath)
      ? JSON.parse(fs.readFileSync(rootPackagePath, 'utf8'))
      : {};

    const runtimePackage = {
      name: 'agentic-sdlc-runtime',
      version: rootPackage.version || VERSION,
      private: true,
      type: 'module',
      description: 'Deployed runtime for the agentic SDLC toolkit.',
      engines: rootPackage.engines || {
        node: '>=20',
      },
      dependencies: rootPackage.dependencies || {},
    };

    fs.writeFileSync(
      path.join(runtimeAbs, 'package.json'),
      `${JSON.stringify(runtimePackage, null, 2)}\n`,
      'utf8'
    );
  }

  copyDir(path.join(root, 'src', 'contracts'), path.join(runtimeAbs, 'contracts'));
  copyDir(path.join(root, 'src', 'templates'), path.join(runtimeAbs, 'templates'));
// sdlc-hardening: data
if (fs.existsSync(path.join(root, 'src', 'schemas'))) {
  copyDir(path.join(root, 'src', 'schemas'), path.join(runtimeAbs, 'schemas'));
}
if (fs.existsSync(path.join(root, 'src', 'policies'))) {
  copyDir(path.join(root, 'src', 'policies'), path.join(runtimeAbs, 'policies'));
}

  const generatedSkills = [];

  for (const skill of skillDefinitions) {
    const skillDir = path.join(skillsAbs, skill.id);
    ensureDir(skillDir);

    const content = renderSkill(skill, {
      cliPath,
      runtimeRoot,
      templatesRoot,
  schemasRoot: `${agentPrefix}${runtimeDir}/schemas`,
  policiesRoot: `${agentPrefix}${runtimeDir}/policies`,
    });

    fs.writeFileSync(path.join(skillDir, 'SKILL.md'), content, 'utf8');

    generatedSkills.push(skill.id);
  }

  const manifest = {
    name: 'agentic-sdlc',
    version: VERSION,
    deployedAt: new Date().toISOString(),
    sourceRoot: root,
    projectRoot: projectRootAbs,
    agentRoot: agentRootRelative || '.',
    runtimeDir,
    skillsDir,
    cliPath,
    runtimeRoot,
    templatesRoot,

    schemasRoot: `${agentPrefix}${runtimeDir}/schemas`,

    policiesRoot: `${agentPrefix}${runtimeDir}/policies`,
    bundled,
    skills: generatedSkills,
  };

  fs.writeFileSync(
    manifestPath,
    `${JSON.stringify(manifest, null, 2)}\n`,
    'utf8'
  );

  if (!bundled && args.install) {
    const result = spawnSync(
      'npm',
      ['install', '--omit=dev', '--no-audit', '--no-fund'],
      {
        cwd: runtimeAbs,
        stdio: 'inherit',
      }
    );

    if (result.status !== 0) {
      fail('npm install failed in deployed runtime directory.');
    }
  }

  const runtimeCliAbs = path.join(runtimeAbs, 'scripts', 'sdlc.mjs');
  const hasNodeModules = fs.existsSync(path.join(runtimeAbs, 'node_modules'));

  let smoke = 'skipped';

  if (!args['skip-smoke'] && (bundled || args.install || hasNodeModules)) {
    const result = spawnSync(
      process.execPath,
      [runtimeCliAbs, '--list-workflows'],
      {
        cwd: projectRootAbs,
        encoding: 'utf8',
      }
    );

    if (result.status !== 0) {
      fail(
        `Deployment smoke test failed.\n${result.stdout || ''}\n${result.stderr || ''}`
      );
    }

    smoke = 'passed';
  }

  console.log(
    JSON.stringify(
      {
        ok: true,
        version: VERSION,
        dest: destAbs,
        projectRoot: projectRootAbs,
        runtime: runtimeAbs,
        skills: skillsAbs,
        cliPath,
        bundled,
        generatedSkills,
        install: Boolean(!bundled && args.install),
        smoke,
      },
      null,
      2
    )
  );
}

main();
